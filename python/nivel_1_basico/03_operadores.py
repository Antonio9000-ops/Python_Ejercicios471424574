# Ejercicios: operadores
# Nivel: básico
# Instrucciones: resuelve cada ejercicio usando variables y operadores aritmeticos,
# de comparacion y logicos. No uses condicionales, bucles, listas ni funciones propias.

# 1. Crea dos numeros enteros y muestra su suma, resta, multiplicacion y division.

# 2. Calcula el residuo de dividir 25 entre 4.

# 3. Calcula 3 elevado a la potencia 4.

# 4. Pide al usuario dos numeros y muestra si el primero es mayor que el segundo.

# 5. Pide al usuario dos numeros y muestra si son iguales.

# 6. Crea dos valores booleanos y muestra el resultado de usar and entre ellos.

# 7. Crea dos valores booleanos y muestra el resultado de usar or entre ellos.

# 8. Crea un valor booleano y muestra su valor contrario usando not.

# 9. Calcula el precio final de un producto aplicando un descuento simple.

# 10. Calcula el area de un triangulo usando base y altura.
